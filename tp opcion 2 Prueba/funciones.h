
#include <stdio.h>

/** \brief Calcula la suma entre dos numeros
 *
 * \param float Sumando A (operando A)
 * \param float Sumando B (operando B)
 * \return float El resultado de la suma entre dos numeros
 *
 */
float SumarNumeros(float, float);

/** \brief Calcula la resta entre dos numeros
 *
 * \param float Minuendo (operando A)
 * \param float Sustraendo(operando B)
 * \return float El resultado de la resta entre dos numeros
 *
 */
float restarNumeros(float, float);
/** \brief Calcula la multiplicacion entre dos numeros
 *
 * \param float Multiplicando A (operando A)
 * \param float Multiplicador A (operando A)
 * \return float El resultado de la multiplicacion entre dos numeros
 *
 */
float multiplicarNumeros(float, float);

/** \brief Calcula la division entre dos numeros
 *
 * \param float El dividendo (operando A)
 * \param float El divisor  (operando B)
 * \return float El resultado de la division entre dos numeros
 *
 */
float dividirNumeros(float, float);

/** \brief calcula el factoria de un numeroentero
 *
 * \param int El valor sobre el cual se calcula el factorial
 * \return int El resultado del factorial del numero
 *
 */
int calcularFactorial(int);


